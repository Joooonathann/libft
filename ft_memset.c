#include "libft.h"

void	*ft_memset(void *p, int v, size_t count)
{
	size_t		i;
	char		*a;

	a = (char *) p;
	i = 0;
	while (i != count)
	{
		a[i] = v;
		i++;
	}
	return (p);
}
